// Exercise 4.3, p. 93: Rewrite reverse to use an array pointer instead
// of a slice.
// Based on rev.

package main

import "fmt"

func main() {
	a := [...]int{0, 1, 2, 3, 4, 5}
	reverse(&a)
	fmt.Println(a) // "[5 4 3 2 1 0]"
}

// reverse reverses an array of ints in place.
func reverse(a *[6]int) {
	for i, j := 0, len(a)-1; i < j; i, j = i+1, j-1 {
		a[i], a[j] = a[j], a[i]
	}
}
